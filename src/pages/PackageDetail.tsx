  function PackageDetail() {
    const { destinationId, packageName } = useParams();
    const [selectedDays, setSelectedDays] = useState(5); // Default to 5 days
    const [searchParams, setSearchParams] = useSearchParams();
  
    // Find the destination and package details
    const destination = destinations.find((dest) => dest.id === parseInt(destinationId));
    const packageDetail = destination?.packages?.find((pkg) => pkg.name === packageName);
  
    if (!packageDetail) {
      return <div className="container py-5 text-center">العرض غير موجود</div>;
    }
  
    // Get the program for the selected number of days
    const program = packageDetail.programs[selectedDays] || { days: [] };
  
    // Handle day selection change
    const handleDayChange = (days) => {
      setSelectedDays(days);
      setSearchParams({ days });
    };
  
    // تقسيم المميزات إلى قسمين: يشمل / لا يشمل
    const includedFeatures = [];
    const excludedFeatures = [];
  
    if (destination.features) {
      destination.features.split("\n").forEach((feature) => {
        if (feature.includes("✔")) {
          includedFeatures.push(feature);
        } else {
          excludedFeatures.push(feature);
        }
      });
    }
  
    return (
      <div className="min-vh-100 d-flex flex-column justify-content-center align-items-center bg-light">
        <div className="text-center w-100">
          <h1 className="mb-5">{packageDetail.name}</h1>
        </div>
    
        <div className="card shadow-lg border-0 w-100 mx-auto rounded-4 overflow-hidden" style={{ maxWidth: "800px" }}>
          <img src={destination.image} className="card-img-top" alt={destination.name} style={{ height: "250px", objectFit: "cover" }} />
  
          <div className="card-body p-4">
            <h3 className="card-title fw-bold text-dark mb-3">{packageDetail.name}</h3>
  
            {/* معلومات السعر والوجهة والمميزات في سطر واحد */}
            <div className="d-flex justify-content-between flex-wrap mb-3">
              <p className="card-text text-danger fw-bold fs-5">
                السعر: {program ? `${program.price} ريال سعودي` : "غير متوفر"}
                <p className="card-text text-muted">
                <strong>الوجهة:</strong> {destination.name}
              </p>
              </p>
  
              
  
              
            </div>
  
            {/* العرض يشمل والعرض لا يشمل */}
            <div className="mb-3">
              <h5 className="text-success fw-bold">✔ العرض يشمل:</h5>
              <ul className="text-muted">
                {includedFeatures.map((feature, index) => (
                  <li key={index}>{feature}</li>
                ))}
              </ul>
  
             
            </div>
  
            {/* Day Selection Buttons */}
            <div className="mt-4">
              <h5 className="fw-bold text-center mb-3 text-dark">اختر عدد الأيام:</h5>
              <div className="d-flex flex-wrap justify-content-center gap-2">
                {[5, 6, 7, 8, 9, 10].map((days) => (
                  <button
                    key={days}
                    className={`btn px-4 py-2 rounded-pill fw-bold transition ${
                      selectedDays === days
                        ? "btn-danger text-white shadow-sm"
                        : "btn-outline-danger"
                    }`}
                    onClick={() => handleDayChange(days)}
                  >
                    {days} أيام
                  </button>
                ))}
              </div>
            </div>
          </div>
        </div>
    
  
        {/* Daily Program Section */}
        <div className="mt-5 w-100" style={{ maxWidth: '1200px' }}>
          <h3 className="text-center mb-4">برنامج الرحلة اليومي ({selectedDays} أيام)</h3>
          <div className="row">
            {program.days.map((dayPkg, index) => (
              <div key={index} className="col-md-4 mb-4">
                <div className="card h-100 shadow border-0 overflow-hidden">
                  <div className="card-body p-4">
                    <h5 className="card-title fw-bold text-dark">{dayPkg.title}</h5>
                    <p className="card-text text-muted">{dayPkg.description}</p>
                  </div>
                </div>
              </div>
            ))}
          </div>
        </div>
      </div>
    );
  }
  
  export default PackageDetail;